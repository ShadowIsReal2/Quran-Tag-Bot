def get_streak_badge(streak: int) -> str:
    """Return the highest earned badge label for the given streak count."""
    if streak >= 365:
        return BADGE_365_DAYS
    if streak >= 100:
        return BADGE_100_DAYS
    if streak >= 30:
        return BADGE_30_DAYS
    if streak >= 7:
        return BADGE_7_DAYS
    return ""


