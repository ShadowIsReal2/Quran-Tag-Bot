def get_daily_motivation(day_seed: int, last_index: int = -1) -> str:
    """
    Return a motivational text that differs from the previous day's.

    Args:
        day_seed:   Integer seed (e.g. day-of-year) for determinism within a day.
        last_index: Index used yesterday; we avoid repeating it.

    Returns:
        A motivational string.
    """
    n = len(DAILY_MOTIVATION_TEMPLATES)
    # Primary pick: day_seed mod n
    idx = day_seed % n
    # If it matches yesterday, shift by 1
    if idx == last_index:
        idx = (idx + 1) % n
    return DAILY_MOTIVATION_TEMPLATES[idx]


